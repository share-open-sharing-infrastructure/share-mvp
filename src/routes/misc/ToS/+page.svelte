<p>Der Leihvertrag entsteht, wie in Abschnitt 2 erläutert, ausschließlich zwischen Verleiher:in und Entleiher:in. AllerLeih hat in diesem Verhältnis weder Rechte noch Pflichten und ist insbesondere nicht zur Streitbeilegung verpflichtet.</p>

<p>Wir empfehlen allen Nutzenden ausdrücklich, vor einer Leihvereinbarung folgende Punkte zu klären:</p>

<ul class="list-disc pl-6 space-y-2">
  <li><strong>Privathaftpflichtversicherung der entleihenden Person.</strong> Frage als Verleiher:in nach, ob die anfragende Person eine Privathaftpflichtversicherung hat. Schäden an entliehenen Gegenständen sind in vielen Privathaftpflichtversicherungen mitversichert (sogenannte „Schäden an gemieteten oder geliehenen Sachen"). Lass dir dies im Zweifel schriftlich bestätigen.</li>
  <li><strong>Pfand bei wertvollen Gegenständen.</strong> Vereinbart bei Gegenständen mit höherem Wert ein Pfand, das die entleihende Person bei der Übergabe hinterlegt und bei der unbeschädigten Rückgabe zurückerhält. Die Höhe des Pfands solltet ihr gemeinsam und situationsangemessen festlegen.</li>
  <li><strong>Schriftliche Übergabe-Notiz.</strong> Bei längerfristigen oder wertintensiven Leihen empfehlen wir, in einer kurzen schriftlichen Notiz Datum, Zustand des Gegenstands, vereinbarten Rückgabetermin und gegebenenfalls Pfandhöhe festzuhalten.</li>
  <li><strong>Klärung im Vorfeld, was bei Beschädigung oder Verlust passiert.</strong> Eine offene Absprache vor Beginn der Leihe verhindert die meisten Streitigkeiten.</li>
</ul>

<p>Diese Empfehlungen sind unverbindlich, aber dringend angeraten. AllerLeih übernimmt keine Verantwortung für Schäden oder Streitigkeiten, die aus dem Verzicht auf solche Absprachen entstehen.</p>

